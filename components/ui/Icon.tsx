/** Noms d'icônes disponibles (autocomplétion + sécurité de type). */
export type IconName = keyof typeof ICONS;

type IconProps = Omit<SVGProps<SVGSVGElement>, "name"> & {
  /** Identifiant de l'icône à afficher. */
  name: IconName;
  /** Taille en pixels (largeur = hauteur). Défaut : 24. */
  size?: number;
};

/**
 * Affiche une icône SVG décorative.
 *
 * Elle est marquée `aria-hidden` : le sens doit toujours être porté par le
 * texte voisin. Pour une icône réellement informative, fournir un `aria-label`
 * et `role="img"` via les props.
 *
 * @param name  Nom de l'icône (cf. {@link IconName}).
 * @param size  Dimension carrée en pixels (défaut 24).
 */
export default function Icon({ name, size = 24, className, ...props }: IconProps) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={1.75}
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
      focusable="false"
      className={className}
      {...props}
    >
      {ICONS[name]}
    </svg>
  );
}
